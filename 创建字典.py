a = dict(one = 1,two = 2, three = 3)              #方法一
b = {'one':1,'two':2,'three':3}                   #方法二
c = dict(zip(['one','two','three'],[1,2,3]))
d = dict([('two',2),('one',1),('three',3)])       #方法三
e = dict({'three':3,'one':1,'two':2})

print (a)
print (b)
print (c)
print (d)
print (e)
